
// import time package
import java.util.Date;

public class HelloWorld {
    public static void main(String[] args) {
        String greeting = "Hello World"; // Storing a string in a variable called "greeting".
        Date date = new Date();

        System.out.println(greeting + " " + date); // Prints the string stored in the variable "greeting" with today's
                                                   // date.
        System.out.println(); // Prints a new line.
    } // End of main method.
} // End of class.